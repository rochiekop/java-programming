public class Komentar {
    public static void main(String[] args) {
        System.out.println(sum(10,20));
    }

    /**
     * function to calculate sum from 2 variable
     * @param value1
     * @param value2
     * @return
     */
    static int sum(int value1, int value2){
        return value1+value2;
    }
}
